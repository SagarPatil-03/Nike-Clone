package pass;

public class Passport {
	
	private int id ;
	@Override
	public String toString() {
		return "Passport [id=" + id + ", name=" + name + "]";
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	private String name ;
	

}
